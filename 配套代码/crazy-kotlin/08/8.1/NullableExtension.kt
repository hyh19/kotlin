// 为可空类型扩展equals方法
fun Any?.equals(other: Any?): Boolean{
	if (this == null) {
		return if (other == null ) true else false
	}
	return other.equals(other)
}
fun main(args: Array<String>) {
	var a = null
	println(a.equals(null)) // 输出true
	println(a.equals("Kotlin")) // 输出false
}